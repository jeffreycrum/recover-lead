"""Fixture-based tests for GeorgiaExcessFundsPdfScraper.

These tests load real PDF bytes generated by tests/fixtures/generate_ga_fixtures.py
and call scraper.parse(bytes) directly.  No pdfplumber mocking is used — the full
parsing pipeline (pdfplumber table extraction → per-county row parser) runs end-to-end.

Regenerate fixtures:
    python tests/fixtures/generate_ga_fixtures.py
"""

from __future__ import annotations

from decimal import Decimal
from pathlib import Path

import pytest

from app.ingestion.georgia_pdf_scraper import GeorgiaExcessFundsPdfScraper

FIXTURES = Path(__file__).parent.parent / "fixtures"


def _load_fixture(filename: str) -> bytes:
    path = FIXTURES / filename
    if not path.exists():
        pytest.skip(f"Fixture not found: {path}  — run generate_ga_fixtures.py")
    return path.read_bytes()


def _make_scraper(county: str, layout: str) -> GeorgiaExcessFundsPdfScraper:
    return GeorgiaExcessFundsPdfScraper(
        county_name=county,
        source_url="https://example.com",
        state="GA",
        config={"layout": layout},
    )


