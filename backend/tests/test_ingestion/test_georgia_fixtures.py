# ─── Cross-layout edge cases ──────────────────────────────────────────────────


class TestGeorgiaScraperEdgeCases:
    """Behavioral edge cases that apply across all layouts."""

    def test_unsupported_layout_raises_runtime_error(self):
        scraper = _make_scraper("Unknown", "nonexistent_layout")
        with pytest.raises(RuntimeError, match="unsupported Georgia PDF layout"):
            scraper.parse(b"%PDF-1.4 fake")

    def test_gwinnett_skips_highlighted_parcels_row(self):
        """Rows where col1 contains 'Highlighted parcels' must be skipped."""
        import io

        from reportlab.lib.pagesizes import letter
        from reportlab.lib.units import inch
        from reportlab.platypus import SimpleDocTemplate, Table, TableStyle

        rows = [
            ["#", "NAME", "PARCEL NUMBER", "OWNER", "SITUS", "EXCESS", "DATE"],
            ["", "Highlighted parcels are pending", "H9999 999Z", "SKIP ME", "123 SKIP RD", "$1.00", "Jan 2020"],
            ["1", "BUYER A", "R0001 001A", "VALID OWNER", "1 VALID ST", "$500.00", "Feb 2021"],
        ]
        buf = io.BytesIO()
        doc = SimpleDocTemplate(buf, pagesize=letter, leftMargin=0.25 * inch, rightMargin=0.25 * inch, topMargin=0.5 * inch, bottomMargin=0.5 * inch)
        table = Table(rows)
        table.setStyle(TableStyle([("GRID", (0, 0), (-1, -1), 0.5, (0, 0, 0)), ("FONTNAME", (0, 0), (-1, -1), "Courier"), ("FONTSIZE", (0, 0), (-1, -1), 7)]))
        doc.build([table])
        pdf_bytes = buf.getvalue()

        scraper = _make_scraper("Gwinnett", "gwinnett")
        leads = scraper.parse(pdf_bytes)

        assert len(leads) == 1
        assert leads[0].case_number == "R0001 001A"

